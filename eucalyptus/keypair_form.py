from django import forms
import re

class KeypairForm(forms.Form):

	#"""半角英数字のみ有効"""
	USERID_REGEX = re.compile(r"^[\w]+$")

	name = forms.RegexField(max_length=256,
								regex=USERID_REGEX,widget=forms.TextInput(attrs={'size':'20'}),
								error_messages={'required': u'接続鍵名を入力してください。','invalid': u'接続鍵名には半角英数字のみ使用可能です。'})


